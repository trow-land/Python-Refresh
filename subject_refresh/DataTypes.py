# Specialised data types
# These are special packages and modules that we can use from librarys


print(2 + 4)
print(2 - 4)
print(2 * 4)
print(2 / 4)

print(type(2 + 4))
print(type(2 / 4))

print(9 / 1.0)
print(type(9 / 1.0))

print(2 ** 2) # 2 to the power of two
print(type(2 ** 2))

print(2 // 4)
print(2 // 4.0)  # Floor division (result gets rounded to nearest whole integer)
print(4 // 2.0)

print(5 % 4)  # Modulo


print(round(3.1))  # Rounds to the nearest number
print(abs(-4))  # Returns the magnitude of the result

